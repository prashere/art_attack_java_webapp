    /**
     * Updates the combo box model based on the selected category. Clears the
     * current model and sets options accordingly.
     */
    private void updateComboBoxModel() {
        DefaultComboBoxModel<String> mediumModel = new DefaultComboBoxModel<>();
        String category = "" + cbCategory.getSelectedItem();
        if ("Acrylic".equals(category) || "Oil".equals(category)) {
            mediumModel.removeAllElements();
            mediumModel.addElement("Canvas");
            mediumModel.addElement("Linen");
            mediumModel.addElement("Wood");
        } else if ("Watercolor".equals(category) || "Charcoal"
                .equals(category) || "Pencil Art".equals(category))
        {
            mediumModel.removeAllElements();
            mediumModel.addElement("Paper");
        } else {
            mediumModel.removeAllElements();
            mediumModel.addElement("Canvas");
            mediumModel.addElement("Linen");
            mediumModel.addElement("Wood");
            mediumModel.addElement("Paper");
        }
        cbMedium.setModel(mediumModel);
    }

    /**
     * Validates the uniqueness of the combination of artwork title and artist
     * name. Checks if the same combination already exists in the table data. If
     * a duplicate is found, displays an error message and clears the input
     * fields.
     *
     * @return true if the combination is unique; false if a duplicate is found.
     */
    private boolean validateArtworkArtist() {
        // Getting the row of table as ArtAttackModel objects
        ArrayList<ArtAttackModel> artworkList = getTableData();

        // Retrieving data from input fields
        String artworkTitle = tfTitle.getText().toLowerCase();
        String artist = tfArtistName.getText().toLowerCase();

        for (ArtAttackModel artAttackModel : artworkList) {
            if (artAttackModel.getArtworkTitle().toLowerCase().equals
        (artworkTitle) && artAttackModel.getArtist().toLowerCase().
                    equals(artist)) {
                JOptionPane.showMessageDialog(null,
                        StringUtil.MESSAGE_SAME_ARTWORK_ARTIST,
                        StringUtil.TITLE_INVALID_INPUT,
                        JOptionPane.ERROR_MESSAGE);
                // Clearing input fields 
                tfArtistName.setText("");
                tfTitle.setText("");
                return false;
            }
        }
        return true;
    }

