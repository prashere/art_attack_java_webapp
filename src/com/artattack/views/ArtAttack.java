    /**
     * Retrieves and returns the data from the entire JTable, converting it into
     * an ArrayList of ArtAttackModel instances. Clears the existing artworkList
     * and populates it by iterating through each row in the JTable.
     *
     * @return An ArrayList of ArtAttackModel instances containing the data from
     * the JTable.
     */
    public ArrayList<ArtAttackModel> getTableData() {
        // Clearing existing data in the artworkList
        artworkList.clear();

        // Iterating through each row in the JTable
        int rows = tblArtwork.getRowCount();
        for (int i = 0; i < rows; i++) {
            // Retrieving ArtAttackModel from the current row
            ArtAttackModel artwork = getTableRowData(i);

            // Adding the artwork to the list if it's not null
            if (artwork != null) {
                artworkList.add(artwork);
            }
        }
        // Returning the populated artworkList
        return artworkList;
    }

    /**
     * Sets the data in the JTable based on the provided ArrayList of
     * ArtAttackModel instances. Clears the existing rows in the table model and
     * populates it with new data.
     *
     * @param artworkList: The ArrayList of ArtAttackModel instances containing
     * the data to be displayed in the JTable.
     */
    public void setTableData(ArrayList<ArtAttackModel> artworkList) {
        // Clearing existing rows of the table
        tableModel.setRowCount(0);
        // Populating the table
        for (ArtAttackModel artwork : artworkList) {
            Object[] rowData = convertArtworkModelToObject(artwork); // function call
            tableModel.addRow(rowData);
        }
    }

