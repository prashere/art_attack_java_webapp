    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        ArrayList<ArtAttackModel> artworkList = getTableData();

        Comparable target = tfSearchValue.getText();
        // Handling the case when the search field is empty
        if (target.equals("")) {
            JOptionPane.showMessageDialog(null, 
                    StringUtil.MESSAGE_EMPTY_SEARCH_FIELD, 
                    StringUtil.TITLE_EMPTY_FIELD, 
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String searchField = "" + cbSearchOptions.getSelectedItem();

        // Sorting the ArrayList based on Search field before performing Binary Search
        MergeSort.setSortAttribute(searchField); // function call
        MergeSort.mergeSort(artworkList); // function call

        BinarySearch.setSearchAttribute(searchField); // function call
        ArrayList<Integer> searchIndexes = BinarySearch.findAllOccurrences
                                    (artworkList, target); // function call
        // Handling the case when no search result is found
        if (searchIndexes.isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    StringUtil.MESSAGE_SEARCH_NOT_FOUND,
                    StringUtil.TITLE_ERROR, 
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        // Clearing any existing selection in the table
        clearSelection();
        // Selecting the rows with search results
        for (Integer searchIndex : searchIndexes) {
            for (int i = 0; i < tableModel.getRowCount(); i++) {
                if (String.valueOf(tableModel.getValueAt(i, 0))
                        .equals(String.valueOf(artworkList.get
                        (searchIndex).getArtworkId()))) {
                    tblArtwork.getSelectionModel().addSelectionInterval
                                                    (i, i);
                }
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

