    /**
     * Handles the action when the "Delete" button is clicked. Asks for user
     * confirmation and deletes the artwork if confirmed.
     *
     * @param artwork: The ArtAttackModel object.
     * @param dialog: The JDialog associated with the artwork details.
     * @param selectedRow: The index of the selected row corresponding to the
     * artwork in the table model.
     */
    private void handleDeleteButton(ArtAttackModel artwork, javax.swing.JDialog 
            dialog, int selectedRow) {
        if (artwork == null) {
            JOptionPane.showMessageDialog(null, 
                    StringUtil.MESSAGE_DELETE_ERROR, 
                    StringUtil.TITLE_ERROR, 
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int option = JOptionPane.showConfirmDialog(
                    null,
                    StringUtil.MESSAGE_CONFIRM_DELETE + artwork.getArtworkId() + "?",
                    StringUtil.TITLE_DELETE,
                    JOptionPane.YES_NO_OPTION);

            if (option == JOptionPane.YES_OPTION) {
                if (selectedRow >= 0 && selectedRow < tableModel.getRowCount()) {
                    tableModel.removeRow(selectedRow);
                    dialog.dispose();
                    JOptionPane.showMessageDialog(null,
                            StringUtil.MESSAGE_DELETE_SUCCESS,
                            StringUtil.TITLE_SUCCESS, 
                            JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, 
                            StringUtil.MESSAGE_DELETE_ERROR, 
                            StringUtil.TITLE_ERROR, 
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, 
                    StringUtil.MESSAGE_DELETE_ERROR, 
                    StringUtil.TITLE_ERROR, 
                    JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * Handles the action when the "Update" button is clicked. Sets up the UI
     * components in the "Update" mode with details from the provided
     * ArtAttackModel for modification.
     *
     * @param artwork: The ArtAttackModel to be updated.
     * @param dialog: The JDialog associated with the artwork details.
     */
    private void handleUpdateButton(ArtAttackModel artwork, javax.swing.JDialog dialog) {
        pnlMainPanel.setTitleAt(2, "Update");
        pnlMainPanel.setSelectedIndex(2);

        tfTitle.setText(artwork.getArtworkTitle());
        tfArtistName.setText(artwork.getArtist());
        cbCategory.setSelectedItem(artwork.getArtworkCategory());
        cbMedium.setSelectedItem(artwork.getArtworkMedium());
        cbArtStyle.setSelectedItem(artwork.getArtStyle());
        SimpleDateFormat inputFormat = new SimpleDateFormat("yyyy/MM/dd");

        try {
            Date date = inputFormat.parse(artwork.getCreationDate());
            dcCreationDate.setDateFormatString("yyyy/MM/dd");
            dcCreationDate.setDate(date);
        } catch (ParseException e) {
            System.out.println("Exception occured for date");
        }

        lblHeightSlider.setText("" + artwork.getArtworkHeight());
        heightSlider.setValue(artwork.getArtworkHeight());
        lblWidthSlider.setText("" + artwork.getArtworkWidth());
        widthSlider.setValue(artwork.getArtworkWidth());
        tfValuation.setText("" + artwork.getArtworkValuation());

        if (artwork.getArtworkLocation().equals(rbCollectorHome.getText())) {
            rbCollectorHome.setSelected(true);
        } else if (artwork.getArtworkLocation().equals(rbExhibition.getText())) {
            rbExhibition.setSelected(true);
        } else {
            rbPrivateCollection.setSelected(true);
        }

        btnAdd.setVisible(false);
        btnUpdateWorking.setVisible(true);

        dialog.dispose();
    }

